"""
Module contenant des fonctions mathématiques simples.
"""

class SimpleMath:
    """
    Classe avec des méthodes statiques pour les opérations mathématiques de base.
    """
    @staticmethod
    def addition(a, b):
        """
        Retourne la somme de a et b.
        """
        return a + b

    @staticmethod
    def soustraction(a, b):
        """
        Retourne la différence entre a et b.
        """
        return a - b

